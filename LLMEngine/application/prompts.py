AGGREGATOR_SYSTEM_PROMPT = """
당신은 IT 교육 강의 품질 리포트를 최종 정리하는 수석 에디터(Report Synthesizer)입니다.

[제약 사항 - 핵심 규칙]
1. 언어 절대 통제: 반드시 **한국어(Korean)**로만 작성하세요. 맥락 없는 외국어가 절대 혼입되지 않도록 극도로 주의하십시오.
2. 10개 강제 및 중복 금지: 파편화된 조각들을 모아 **정확히 10개**의 다각도 분석 리스트를 완성하십시오. 단어를 살짝 바꿔 똑같은 의미의 문장을 늘어놓는 동어반복 행위를 엄격히 금지합니다.
3. 모순 제거 (Anti-Contradiction): 점수표의 수치와 반대되는 거짓 칭찬이나 거짓 비판을 절대 작성하지 마십시오.
4. 톤앤매너: 전문 교육 컨설턴트처럼 격식 있고 분석적인 문체(~함, ~임)를 사용하세요.
"""

def build_aggregator_user_prompt(items: list[str], category: str) -> str:
    items_text = "\n".join([f"- {item}" for item in items])
    return f"""
아래 리스트는 강의 전체 구간에서 수집된 {category} 목록입니다.
의미가 겹치는 것을 통합하고, 중복을 제거하되 **정확히 10개의 독립적인 문장**으로 구성해 주세요.

[{category} 목록]
{items_text}

[출력 형식]
다른 설명 없이 요약된 문장들만 한 줄에 하나씩 리스트 형태로 출력하세요.
"""

def build_aggregator_refine_prompt(all_items: list[str], label: str, scores_context: str = "", trends: str = "") -> str:
    combined_text = "\n".join([f"- {item}" for item in all_items if "특이사항 없음" not in item])
    if not combined_text.strip():
        combined_text = "- 수집된 직접적인 텍스트 코멘트가 부족합니다. 아래의 [13개 항목 평균 점수] 데이터에 전적으로 의존하여 리포트를 생성하십시오."

    if label == "강점":
        strategy = """
[강점 도출 전략: 13개 지표 1:1 매핑법]
1. 제공된 [13개 평가 항목 평균 점수] 데이터에서 점수가 가장 높은 상위 10개의 항목을 선별하십시오.
2. 선택된 10개의 항목 각각에 대하여, 해당 항목의 특징이 잘 드러나도록 정확히 1문장씩 강점 코멘트를 작성하십시오.
3. 결과적으로 서로 다른 10개의 평가 지표를 다루는 10줄의 리스트가 생성되어야 합니다. (예: 1번 줄은 '설명 순서' 칭찬, 2번 줄은 '예시 적절성' 칭찬 등)
4. 이 방식을 통해 '예시가 좋다'는 똑같은 말을 단어만 바꿔 여러 번 쓰는 동어반복을 완벽히 차단하십시오.
"""
    elif label == "이슈":
        strategy = """
[이슈 도출 전략: 13개 지표 1:1 매핑법]
1. 제공된 [13개 평가 항목 평균 점수] 데이터에서 점수가 가장 낮은 하위 10개의 항목을 선별하십시오.
2. 선택된 10개의 항목 각각에 대하여, 해당 항목에서 무엇이 부족했는지 구체적인 개선점(이슈)을 정확히 1문장씩 작성하십시오.
3. 결과적으로 서로 다른 10개의 평가 지표를 다루는 10줄의 리스트가 생성되어야 합니다. (예: 1번 줄은 '학습 목표 누락' 비판, 2번 줄은 '참여 유도 부족' 비판 등)
4. 이 방식을 통해 중복된 비판을 방지하고 다각도의 개선 가이드를 10개 꽉 채워 제공하십시오.
"""
    else:
        strategy = ""

    trends_block = f"\n[점수 편차 메타데이터]\n{trends}\n" if trends and trends.strip() else ""
    return f"""당신은 IT 교육 리포트 전문가입니다. 아래는 강의 전체에 대한 정량적/정성적 평가 데이터입니다.
{trends_block}
[13개 평가 항목 전체 평균 점수]
{scores_context}

[청크별 수집된 팩트 조각 (Raw {label})]
{combined_text}

[지시사항]
{strategy}
5. 작성 완료 후 직접 항목의 개수를 세어보십시오. 반드시 정확히 10개여야 합니다. 

[출력 형식]
다른 설명 없이 완성된 문장들만 한 줄에 하나씩 리스트 형태로 출력하세요.
"""